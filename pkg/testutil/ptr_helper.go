package testutil

// PTRHelper เป็นการใช้ Generics มีวัตถุประสงค์หลักคือการ
//
//	สร้าง Pointer (*T) จากค่า (Value T) ที่ส่งเข้าไป
func PTRHelper[T any](v T) *T {
	return &v
}

// "Generics คือเครื่องมือที่อนุญาตให้คุณเขียนโค้ด
// (เช่น ฟังก์ชัน หรือโครงสร้างข้อมูล/Struct)
// ที่สามารถทำงานได้กับ หลายประเภทข้อมูล (Data Types)
// โดยไม่ต้องระบุประเภทนั้นๆ อย่างเจาะจงในตอนที่เขียนโค้ด"
